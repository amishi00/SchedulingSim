# ── Shared simulation parameters ─────────────────────────────────────────────
N                 = 1000   # number of jobs
MEAN_INTERARRIVAL = 1.0    # mean inter-arrival time
MEAN_SIZE         = 3.0    # mean job size
SEED              = 3      # random seed

# Swap in any rank_data you want here; modules fall back to their own if not passed.
# Example: LAS identity rank   → [(i/100, i/100) for i in range(10000)]
# Example: default (from naive) → naive.rank_data  (after import)
RANK_DATA         = RANK_PREPROVIDED   # None = each module uses its own built-in rank_data
# ─────────────────────────────────────────────────────────────────────────────


_parser = argparse.ArgumentParser(description="Run LAS event-driven, naive, bisect, and bisect+numpy "
                                              "for k=1..max_k and plot runtimes.")
_parser.add_argument("max_k", type=int, help="Upper bound for k (inclusive)")
_args = _parser.parse_args()

k_values     = list(range(1, _args.max_k + 1))
naive_times  = []
bisect_times = []
np_times     = []

print(f"\n{'='*50}")

#if you want to run LAS event-driven, uncomment the following lines and the corresponding lines in the loop below. Note that LAS event-driven is not k-dependent, so we only run it once (after the loop) for a single k value (e.g., k=1).
# print(f"[LAS Event-Driven] (single run, k-independent)")
# las_time = las_event_driven.run(N=N, mean_interarrival=MEAN_INTERARRIVAL, mean_size=MEAN_SIZE, seed=SEED)

for k in k_values:
    print(f"\n{'='*50}")
    print(f"[Naive] k={k}")
    kw = {} if RANK_DATA is None else {'rank_data': RANK_DATA}
    t = naive.run(k, N=N, mean_interarrival=MEAN_INTERARRIVAL, mean_size=MEAN_SIZE, seed=SEED, **kw)
    naive_times.append(t)

    print(f"\n[Bisect] k={k}")
    t = unoptimized_bisect.run(k, N=N, mean_interarrival=MEAN_INTERARRIVAL, mean_size=MEAN_SIZE, seed=SEED, **kw)
    bisect_times.append(t)

    print(f"\n[Bisect+NumPy] k={k}")
    t = bisect_np.run(k, N=N, mean_interarrival=MEAN_INTERARRIVAL, mean_size=MEAN_SIZE, seed=SEED, **kw)
    np_times.append(t)

print(f"\n{'='*50}")
print(f"{'k':<6} {'Naive (s)':<14} {'Bisect (s)':<14} {'Bisect+NP (s)'}")
print(f"{'-'*6} {'-'*14} {'-'*14} {'-'*13}")
for k, tn, tb, tnp in zip(k_values, naive_times, bisect_times, np_times):
    print(f"{k:<6} {tn:<14.4f} {tb:<14.4f} {tnp:.4f}")
# print(f"\nLAS Event-Driven: {las_time:.4f}s")

fig, ax = plt.subplots()
ax.plot(k_values, naive_times,  marker='o', label='Naive')
ax.plot(k_values, bisect_times, marker='s', label='Bisect')
ax.plot(k_values, np_times,     marker='^', label='Bisect + NumPy')
# ax.axhline(y=las_time, linestyle='--', color='green',
#            label=f'LAS Event-Driven ({las_time:.3f}s, single run)')
ax.set_xlabel('k')
ax.set_ylabel('Wall-clock time (seconds)')
ax.set_title(f'Runtime vs k')
fig.suptitle(f'N={N}, seed={SEED}, λ=1/{MEAN_INTERARRIVAL}, μ=1/{MEAN_SIZE}', x=0.98, ha='right', fontsize=9)
ax.legend()
plt.tight_layout()
plt.show()

